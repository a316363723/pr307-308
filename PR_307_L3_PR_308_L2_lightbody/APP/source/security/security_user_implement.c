static void Security_Flash_Erase(uint32_t addr)
{
	EFM_Unlock();
	while(Set != EFM_GetFlagStatus(EFM_FLAG_RDY0));
	if(Ok != EFM_SectorErase(addr) )
	{
	}
	EFM_Lock();
}
