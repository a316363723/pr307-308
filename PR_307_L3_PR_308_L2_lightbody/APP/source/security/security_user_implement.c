static void Security_function_cache_Write(uint32_t dst, uint8_t *src)
{
		uint8_t i = 0;
		uint32_t* p = (uint32_t*)src; 
	
		EFM_Unlock();
		EFM_OTP_WP_Unlock();
		EFM_SectorErase(FUNCTION_ENCRYPT_ADDRESS);
		//EFM_Cmd(EFM_FLASH0_ACT_FLASH1_STP);
		(void)EFM_SectorCmd_Single(((uint32_t)dst/0x2000), Enable);
//		while(Set != EFM_GetFlagStatus(EFM_FLAG_RDY0))
//		{
//			__NOP();
//		}
		for(; i < 4; i++)
		{
			__disable_irq();
			EFM_SingleProgram(dst + i*4, *p);
		   __enable_irq();
		   p++;
		} 
		(void)EFM_SectorCmd_Single(((uint32_t)dst/0x2000), Disable);
		EFM_Lock();
}

