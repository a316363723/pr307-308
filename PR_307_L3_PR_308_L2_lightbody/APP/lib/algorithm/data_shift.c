/*****************************************************************************************
* Function Name: HalfWordBufForwordShift
* Description  : 半字数组以某节点为起点向前移位固定单元.
* Arguments    : 起始节点、移位单元、数组总长度、数组首地址（start_node >= shift_unit）
* Return Value : NONE
******************************************************************************************/
void HalfWordBufForwordShift(uint16_t start_node, uint16_t shift_unit, uint16_t len, uint16_t buf[len])
{
    uint16_t i = 0;
    /*将要被填充的位置清零*/
    for(i = 0; i < start_node; i++)
    {
        buf[i] = 0;
    }
    /*把数据向前移位，移位后的位置清零*/
    for(i = start_node; i < len; i++)
    {
        buf[i - shift_unit] = buf[i];
        buf[i] = 0;
    }
}
